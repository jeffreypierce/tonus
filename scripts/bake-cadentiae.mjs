// ---------------------------------------------------------------------------
// scripts/bake-cadentiae — generate src/data/cadentiae.ts from the corpus tally
// ---------------------------------------------------------------------------
// Reads working/qa-sweep/cadfams-omni.json (produced by
// working/qa-sweep/mine-cadentiae-omni-2.mjs + aggregate-cadentiae-2.py) and
// bakes the CADENTIAE appendix table: every (shape, arrival) family with
// n >= FLOOR, with its corpus statistics and — where one exists — its draft
// Latin binomial. Names are DRAFTS pending the editorial naming pass.
//
// Run from repo root:  node scripts/bake-cadentiae.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dir = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(__dir, "../working/qa-sweep/cadfams-omni.json");
const OUT = resolve(__dir, "../src/data/cadentiae.ts");

// Families below this corpus count stay in the qa artifact, out of the table.
// At 150 the table carries ~61% of all phrase-ends in ~117 families.
const FLOOR = 150;

// Draft binomials, by shape signature. The first ten are the 2026-07-08
// GR-mining drafts (working/design-analysis-track.md §CADENTIAE); the two
// mirrors (circuitus infra, tactus supra) and the semitone-fall foldings
// (-1,-2,0 · -2,-1,-2) are 2026-07-17 drafts. ALL names await the naming pass.
const NOMINA = new Map([
  ["2,0,-2", "circuitus supra"], // touch above, fall home
  ["-2,2,0", "circuitus infra"], // dip below, return home (mirror)
  ["2,-2,0", "tactus supra"], // touch above, settle with repeated arrival
  ["-2,0,-2", "descensus repercussus"], // fall with repeated note
  ["-2,-2,0", "descensus simplex"], // plain stepwise fall
  ["-2,-2,2", "descensus simplex"], // stepwise fall, lift onto the close
  ["-2,2,-2", "flexus"], // the turn
  ["-5,0,-2", "lapsus quartae"], // leap-off suspension
  ["0,0,0", "repercussio"], // the hammered close
  ["2,1,-3", "elevata protica"], // lift then third-fall
  ["2,-4,2", "ascensus deuteri"], // rise INTO the final
  ["1,-1,-2", "semitonium circumactum"], // the mi–fa turn
  ["-2,-1,0", "descensus per semitonium"], // down through mi
  ["0,0,-1", "descensus per semitonium"],
  ["-2,0,-1", "descensus per semitonium"],
  ["-1,-2,0", "descensus per semitonium"], // semitone-first fall (mode-1 home)
  ["-2,-1,-2", "descensus per semitonium"], // fall through the semitone
]);

// The arrival case, mode-independent (the classifier upgrades a tenor-degree
// arrival to "in tenorem" per mode at detection time). Arrivals are
// octave-reduced to [-5..+6] by the mining method.
export function adventusCase(arrival) {
  if (arrival === 0) return "in finalem";
  if (arrival === 1 || arrival === 2) return "in secundam";
  if (arrival === 3 || arrival === 4) return "in tertiam";
  if (arrival === 5 || arrival === 6) return "in quartam";
  if (arrival === -1 || arrival === -2) return "in subfinalem";
  if (arrival === -3 || arrival === -4) return "in subtertiam";
  return "in subquartam"; // -5
}

const tally = JSON.parse(readFileSync(SRC, "utf8"));
const rows = tally.families.filter((f) => f.n >= FLOOR);
const kept = rows.reduce((s, r) => s + r.n, 0);

const entries = rows.map((f) => {
  const [shapeStr, arrStr] = f.key.split(" @");
  const shape = shapeStr.split(",").map(Number);
  const arrival = Number(arrStr);
  return {
    key: f.key,
    familia: NOMINA.get(shapeStr) ?? null,
    shape,
    arrival,
    adventus: adventusCase(arrival),
    n: f.n,
    finality: f.finality,
    modes: f.modes,
  };
});

const stamp = new Date().toISOString();
const body = entries
  .map(
    (e) =>
      `  ${JSON.stringify({
        key: e.key, familia: e.familia, shape: e.shape, arrival: e.arrival,
        adventus: e.adventus, n: e.n, finality: e.finality, modes: e.modes,
      })},`,
  )
  .join("\n");

const file = `// AUTO-GENERATED by scripts/bake-cadentiae.mjs — do not edit manually
// Generated: ${stamp}
// ---------------------------------------------------------------------------
// data/cadentiae — the corpus-grounded cadence catalogue (CADENTIAE)
// ---------------------------------------------------------------------------
// Every phrase-end in the corpus (gr, lu, la, lh, am, nr, ky — ${tally.phraseEnds}
// ends from the full retrievable repertory), keyed by the last <=4 notes'
// interval signature in semitones (the shape — the gesture) and the closing
// note's offset from the chant final, octave-reduced to [-5..+6] (the arrival —
// the function). Families with n >= ${FLOOR} are tabled here (${entries.length} families,
// ${((kept / tally.phraseEnds) * 100).toFixed(1)}% of all phrase-ends); the full tally lives in the mining
// artifact. finality = share of a family's occurrences at a final close
// ("::" or chant end) — measured function, continuous, corpus-derived.
//
// This is inventa, not tradita: a computed catalogue with stated method and
// open code ("tonus catalogue, method §cadentiae"). The shape binomials are
// DRAFTS pending the editorial naming pass; unnamed families carry null.
// Method + display doctrine: working/design-analysis-track.md §CADENTIAE.

/** One corpus cadence family: a (shape, arrival) pair with its statistics. */
export interface CadentiaFamilia {
  /** "shape @arrival", e.g. "2,0,-2 @0" — joins mining/census artifacts. */
  key: string;
  /** Draft Latin binomial for the shape (the gesture), or null if unnamed. */
  familia: string | null;
  /** Interval signature in semitones between the tail's consecutive notes. */
  shape: number[];
  /** Closing note minus chant final, semitones, octave-reduced to [-5..+6]. */
  arrival: number;
  /** The arrival case, mode-independent: "in finalem", "in tertiam", ... */
  adventus: string;
  /** Corpus occurrences. */
  n: number;
  /** Share of occurrences at a final close — the measured finality index. */
  finality: number;
  /** Occurrences by mode digit ("1".."8"; "?" = mode-less chants). */
  modes: Record<string, number>;
}

/** The cadence catalogue, most frequent family first. */
export const CADENTIAE: CadentiaFamilia[] = [
${body}
];
`;

writeFileSync(OUT, file);
console.log(`baked ${entries.length} families (${((kept / tally.phraseEnds) * 100).toFixed(1)}% of ${tally.phraseEnds} ends) -> src/data/cadentiae.ts`);
